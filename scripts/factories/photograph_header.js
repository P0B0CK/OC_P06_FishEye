function photographerHeader() {
    const photographerBanner = document.getElementsByClassName('photograph-header');
    
    let infoMembers = document.createElement('div');
    let contactMembers = document.createElement('div');
    let avatarMembers = document.createElement('div');

    infoMembers.setAttribute('class', 'infoMembers');
    contactMembers.setAttribute('class', 'contactMembers');
    avatarMembers.setAttribute('class', 'avatarMembers');

    photographerBanner.appendChild('newDiv');
    
}